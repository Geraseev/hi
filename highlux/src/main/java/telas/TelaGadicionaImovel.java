    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{
            Imovel obj = new Imovel();

            obj.setLogradouro(txtLog.getText());
            obj.setNumero(txtNum.getText());
            obj.setBairro(txtBairro.getText());
            obj.setCidade(txtCid.getText());
            obj.setEstado(txtEst.getText());
            obj.setCep(txtCep.getText());
            obj.setComplemento(txtComplemento.getText());

            ImovelDAO dao = new ImovelDAO();
            dao.adicionarExtra(obj);

            JOptionPane.showMessageDialog(null, "cadastro realizado com sucesso");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "falha" + e);
        }
        try {
            String logradouro = txtLog.getText();

            ImovelDAO dao = new ImovelDAO();
            List<Imovel> listaI = dao.listarEnderecoPorLog(logradouro);
            DefaultTableModel modelo = (DefaultTableModel)tabela1.getModel();
            modelo.setNumRows(0);

            for(Imovel v : listaI){
                modelo.addRow(new Object[]{
                    v.getIdendereco(),
                    v.getLogradouro(),
                    v.getCidade(),
                    v.getEstado()
                });
            }

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton2ActionPerformed

